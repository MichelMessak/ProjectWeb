package com.itc.repository;

/**
 *
 * @author ANGEL
 */
public class FC4RepositoryException extends Exception {

    /**
     * Creates a new instance of <code>FC4RepositoryException</code> without detail message.
     */
    public FC4RepositoryException() {
    }


    /**
     * Constructs an instance of <code>FC4RepositoryException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public FC4RepositoryException(String msg) {
        super(msg);
    }
}
